<?php
echo "Output: LOOPING PERTAMA:<br><br> ";

// Looping pertama (Ascending)
for ($a = 2; $a <= 20; $a += 2) {
    echo "$a - I Love PHP<br>";
}

echo "<br>LOOPING KEDUA:<br><br> ";

// Looping kedua (Descending)
for ($a = 20; $a >= 2; $a -= 2) {
    echo "$a - I Love PHP<br>";
}
?>
